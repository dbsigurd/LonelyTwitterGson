package ca.ualberta.cs.lonelytwitter;


public class Summary
{
	private long avgNumTweet;
	
	private long avglength;
	
	public Summary(){
		super();
	
		this.avglength= avglength;
		this.avgNumTweet =avgNumTweet;
}

	
	public long getAvgNumTweet()
	{
	
		return avgNumTweet;
	}

	
	public void setAvgNumTweet(long avgNumTweet)
	{
	
		this.avgNumTweet = avgNumTweet;
	}

	
	public long getAvglength()
	{
	
		return avglength;
	}

	
	public void setAvglength(long avglength)
	{
	
		this.avglength = avglength;
	}
}

